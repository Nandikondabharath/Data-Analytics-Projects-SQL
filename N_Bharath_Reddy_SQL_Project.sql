-- KEY INSIGHTS
-- Based on: 200 orders | 50 customers | 10 restaurants
-- Data range: Jan 2025 - May 2025 (May is partial, only 1 day of data)
-- ============================================================

-- 1) HIGH CANCELLATION RATE
--    94 of 200 orders (47%) are Cancelled; only 106 (53%) are Completed.
--    This is the single biggest red flag in the dataset - almost 1 in 2
--    orders never completes. Recommend investigating whether cancellations
--    cluster around a specific restaurant, payment method, or time window.

-- 2) REVENUE IS CONCENTRATED IN ONE RESTAURANT
--    Tandoori Nights generates 26,602 in revenue - 19.75% of total platform
--    revenue (134,756) from a single restaurant out of 10. It also has the
--    highest order volume of any restaurant in any city (34 orders in Delhi,
--    per Q14). Recommend treating it as a key partner and studying what
--    drives its performance (pricing, rating, delivery speed) to replicate
--    with mid-tier restaurants.

-- 3) ONLY 3 OF 10 RESTAURANTS BEAT THE PLATFORM AVERAGE ORDER VALUE
--    Platform-wide average order value is 673.78. Only Tandoori Nights
--    (782.41), BBQ Nation (744.44), and Spice Hub (735.92) exceed it -
--    meaning 70% of restaurants underperform on order value and may need
--    menu pricing or upsell support.

-- 4) CITY-LEVEL ORDER VALUE GAP
--    Mumbai has the highest average order value (735.92) while Hyderabad
--    has the lowest (610.67) - a 20.5% gap between the top and bottom city.
--    Worth checking whether this reflects restaurant mix, customer income
--    profile, or delivery radius differences.

-- 5) CUSTOMER BASE IS FULLY ACTIVE - NO DORMANT SIGNUPS
--    Q7 returns zero results: all 50 signed-up customers have placed at
--    least one order. Retention isn't the current problem area - order
--    completion (see insight #1) is.

-- 6) TOP 5 CUSTOMERS DRIVE ~19% OF REVENUE
--    The top 5 spenders (of 50 customers, 10%) contributed 25,847 combined -
--    19.18% of total revenue. Not extreme concentration, but still worth a
--    loyalty/retention push for this group, led by Nandini Rao (5,894).

-- 7) DEBIT CARD IS THE DOMINANT PAYMENT METHOD
--    Debit Card is used in 63 of 200 orders (31.5%), ahead of UPI (24%),
--    Cash (23%), and Credit Card (21.5%). Fairly balanced across methods,
--    but Debit Card should be the top priority for uptime/reliability since
--    it handles the largest single share of transactions.

-- 8) ORDER FREQUENCY: 38% OF CUSTOMERS ORDER MORE THAN AVERAGE
--    Average orders per customer is 4. 19 of 50 customers (38%) exceed this,
--    topping out at 8 orders (Nandini Rao). A healthy sign of repeat usage
--    across a meaningful share of the base, not just a handful of outliers.

-- 9) REVENUE PEAKED IN MARCH, THEN DECLINED
--    Monthly revenue: Jan 30,933 -> Feb 32,772 -> Mar 38,650 (peak) ->
--    Apr 31,111 -> May 1,290. March was the strongest month (+18% vs Jan),
--    but April dropped ~19.5% from the peak. Note: May's low figure reflects
--    only 1 day of data in this export, not a real decline - exclude it from
--    trend conclusions until a full month is available.
